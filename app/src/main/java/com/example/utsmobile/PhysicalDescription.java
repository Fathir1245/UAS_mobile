package com.example.utsmobile;

public class PhysicalDescription {
    private String size;
    private float width;
    private int pageContent;
    private int pageCover;
    private int coverWeight;
    private String coverMaterial;
    private String coverFinishing;
    private String paperType;
    private int paperWeight;
    private float backOfBook;
    private String bindingTechnique;

    // Getter methods for each field
    public String getSize() {
        return size;
    }

    public float getWidth() {
        return width;
    }

    public int getPageContent() {
        return pageContent;
    }

    public int getPageCover() {
        return pageCover;
    }

    public int getCoverWeight() {
        return coverWeight;
    }

    public String getCoverMaterial() {
        return coverMaterial;
    }

    public String getCoverFinishing() {
        return coverFinishing;
    }

    public String getPaperType() {
        return paperType;
    }

    public int getPaperWeight() {
        return paperWeight;
    }

    public float getBackOfBook() {
        return backOfBook;
    }

    public String getBindingTechnique() {
        return bindingTechnique;
    }
}
